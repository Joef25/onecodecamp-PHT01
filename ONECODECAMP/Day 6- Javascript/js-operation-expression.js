//Arithmetic Operation
let x = 10;
let y = 5;

//sum or total '+'
let sum = x + y;
console.log("The sum is:", sum);

//difference '-'
let dif = x - y;
console.log("The dif is:", dif);

//Product
let prod = x * y;
console.log("The product is:", prod);

//Quotient
let div = x / y;
console.log("The Quotient is:", div);

//Remainder or modulo
let remainder = x % y;
console.log("The remainder is:", remainder);

let answer = 10 + 2 * 23 / 2;
console.log("The answer is", answer);

//concatenation
let a = "Hello";
let b = "World!";
console.log(a + " " + b);

//Assignment Expression
let i = 40;
let j = 20;


//Assignment Expression '+'
i += j;
console.log(i)

//Assignment Expression '-'

i -= j;
console.log(i)

//Assignment Expression '*'

i *= j;
console.log(i)

//Assignment Expression '/'

i /= j;
console.log(i)